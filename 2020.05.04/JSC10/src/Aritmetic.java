public class Aritmetic {
    public static void main(String[] args) {
        int i = 2 * 4;
        int j = 5 + 6;
        System.out.println(i);
        System.out.println(8 - 3);
        i = 3 + j;
        System.out.println(i);
        System.out.println(9 % 2);
        i = i + 1;
        System.out.println(i);
        i++; // léptető operátor
        System.out.println(i);
        i = 6 + i;
        i += 6; // i = i + 6;
        // = érték adó utasítás; 
    }
}
